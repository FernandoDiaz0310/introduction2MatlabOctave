%% Script VS. Live Scripts
% MATLAB provee de dos formas de guardar código: mediante un script y mediante 
% un live script.
% 
% Las principales diferencias son: 
% ** Los scritps son únicamente para escribir código, mientras que los live script 
%    permiten ingresar texto personalizado además del código. Es decir, segmenta 
%    bloques de código y de texto.
% ** Los live scripts permiten insertar imágenes, ecuaciones, etc.
% ** Los live scripts, en los bloques de código, permiten trabajar con controles. 
% ** Los scripts obligatoriamente trabajan con la ventana de comandos, mientras 
%    que los live scritps tienen su propia zona de despliegue. Se utiliza la ventana 
%    de comandos únicamente para el ingreso de datos del usuario.
%% 
% El uso de scritps o live scripts es indistinto. Es decir, para escribir código 
% ambos tipos de archivos cumplen esta función. Pero si se quiere personalizar 
% los códigos, dar mayor detalle y mejorar la documentación de este, se recomienda 
% utilizar live scripts.